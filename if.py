# if / elif / else

# se / se não se/ se não
entrada = input('Você quer "entrar "ou "sair"do sistema?')


if entrada == 'entrar':
    print('Você entrou no sistema') #digitanto 'entrar' vai aparecer a seguinte frase 'Você entrou no sistema'  IF = SE
    
elif entrada == 'sair':
    print('Você saiu do sistema') # Caso digite 'sair' vai aparecer esta outra fase 'Você saiu do sistema' ELIF = se não se
    
else: print('Não entendi o que você quer') # como não foi nenhuma das opçoes acima caiu na oppção ELSE = SE NÃO 'não entendi'
